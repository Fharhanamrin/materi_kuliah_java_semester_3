package pertemuan_6;

public class Pertemuan_6 {
    // Operasi Matematika
    public static void main(String[] args) {
        System.out.println("Pertemuan 6 Java");
        System.out.println(Math.max(10,20));//memilih nilai yang lebih besar
        System.out.println(Math.min(10,20));//memilih nilai yang lebih kecil
        System.out.println(Math.pow(10,20));//hasil perpangkatan a dan b
        System.out.println(Math.sqrt(10));//akar quardrar
    }
}
